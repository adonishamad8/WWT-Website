        {{-- 5. Labour Standards Policy --}}
        <h2 class="h4 mt-5">5. Labour Standards Policy</h2>
        <p class="text-muted">In alignment with the UNGC, ILO Core Conventions &amp; ISO 9001</p>

        <p>
            WWTT protects and advances internationally recognized labour rights across all operations and business
            relationships, embedding UNGC labour principles into HR systems, supplier management processes, and
            governance structures.
        </p>

        <h3 class="h5 mt-3">Key Commitments</h3>
        <ul>
            <li><strong>Child Labour:</strong> Prohibit all forms of child labour in accordance with ILO Conventions and require suppliers to certify that no child labour exists in their operations.</li>
            <li><strong>Forced or Compulsory Labour:</strong> Prohibit forced labour, human trafficking, retention of documents, or involuntary work; ensure employees may freely terminate employment with reasonable notice.</li>
            <li><strong>Health &amp; Safety:</strong> Provide a safe and healthy workplace consistent with international guidelines, including training on risk assessment and emergency response.</li>
            <li><strong>Freedom of Association:</strong> Respect employees’ rights to form or join unions and engage in collective bargaining without retaliation.</li>
            <li><strong>Non-Discrimination &amp; Equal Opportunity:</strong> Prohibit discrimination in recruitment, pay, promotion, or training and uphold gender equality and diversity.</li>
            <li><strong>Disciplinary Practices:</strong> Prohibit corporal punishment, mental or physical coercion, or degrading treatment.</li>
            <li><strong>Working Hours:</strong> Comply with applicable labour laws and ILO standards relating to working hours, rest breaks, and holidays.</li>
            <li><strong>Remuneration:</strong> Provide wages and benefits in compliance with national laws and fair labour practices, with transparent, documented employment relationships.</li>
        </ul>

        {{-- 6. Sustainability Policy --}}
        <h2 class="h4 mt-5">6. Sustainability Policy</h2>
        <p class="text-muted">In alignment with the UNGC, ISO 9001, ISO 14001 &amp; the SDGs</p>

        <p>
