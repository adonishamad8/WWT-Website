            <li><strong>Social Sustainability:</strong> Strict non-discrimination, gender equality, fair labour practices, strong occupational health &amp; safety, and support for community development initiatives.</li>
            <li><strong>Economic Sustainability:</strong> Sustainability criteria in tendering, ethical governance, robust internal controls, and zero tolerance for bribery and fraud.</li>
            <li><strong>Supply Chain Sustainability:</strong> Requiring suppliers to sign WWTT’s Sustainability &amp; Ethics Code of Conduct and assessing them on environmental and social performance.</li>
            <li><strong>Continuous Improvement &amp; Reporting:</strong> Annual UNGC Communication on Progress (CoP), quantitative sustainability KPIs, and benchmarking against leading global standards.</li>
        </ul>

