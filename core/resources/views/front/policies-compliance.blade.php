            <li><strong>Gender-Sensitive Decision-Making:</strong> Integrate gender considerations into corporate strategy, policies, and leadership decisions.</li>
            <li><strong>Dignity, Safety &amp; Anti-Harassment:</strong> Enforce zero tolerance for harassment, bullying, discrimination, or victimization.</li>
            <li><strong>Inclusion of Women with Disabilities:</strong> Ensure full inclusion and accessibility across workplaces and programmes.</li>
            <li><strong>Community Empowerment:</strong> Promote women’s economic participation, independence, and social integration.</li>
