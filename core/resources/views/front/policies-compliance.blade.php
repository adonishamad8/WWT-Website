        </p>

        <h3 class="h5 mt-3">Key Commitments</h3>
        <ul>
            <li><strong>Non-Discrimination:</strong> All employment decisions (hiring, promotion, compensation, training, termination) are based solely on merit, qualifications, and performance.</li>
            <li><strong>Equal Opportunity:</strong> Provide equitable access to jobs, learning opportunities, leadership roles, and professional growth.</li>
            <li><strong>Accessibility &amp; Inclusion:</strong> Maintain premises, services, and digital tools that accommodate people with disabilities.</li>
            <li><strong>Fair Recruitment:</strong> Use transparent, inclusive hiring processes that consider transferable and life experience, not only formal qualifications.</li>
            <li><strong>Respect &amp; Dignity:</strong> Ensure all staff and clients are treated with respect and prohibit harassment, bullying, abuse, or degrading behaviour.</li>
            <li><strong>Gender Equality &amp; Diversity:</strong> Promote gender balance and diversity at all organizational levels.</li>
            <li><strong>Protection of Vulnerable Groups:</strong> Ensure safeguards for minority groups, young workers, migrants, and people with disabilities.</li>
            <li><strong>Monitoring &amp; Improvement:</strong> Track diversity indicators, recruitment fairness, and staff training as part of UNGC CoP reporting.</li>
        </ul>

