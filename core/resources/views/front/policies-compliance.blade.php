        {{-- 1. Anti-Corruption Policy --}}
        <h2 class="h4 mt-4">1. Anti-Corruption Policy</h2>
        <p class="text-muted">In alignment with the United Nations Global Compact (UNGC) and the United Nations Convention against Corruption (UNCAC)</p>

        <p>
            WWTT adopts a zero-tolerance approach to bribery, fraud, extortion, and any form of corrupt practice,
            ensuring that integrity, transparency, and accountability remain core principles of our culture and operations.
            No employee, representative, or partner shall offer, solicit, give, or receive any undue advantage, monetary
            or otherwise, to influence a decision or gain improper benefit.
        </p>

        <h3 class="h5 mt-3">Key Commitments</h3>
        <ul>
            <li><strong>Legal Compliance:</strong> Ensure compliance with the UN Convention against Corruption, UNGC Principle 10, and all applicable national anti-bribery laws.</li>
            <li><strong>Zero Tolerance:</strong> Strict prohibition of all forms of bribery, facilitation payments, kickbacks, and conflicts of interest.</li>
            <li><strong>Transparency &amp; Record Keeping:</strong> Maintain truthful, complete, and auditable financial records and prohibit any off-the-books transactions.</li>
            <li><strong>Due Diligence:</strong> Conduct due diligence on agents, suppliers, and partners to confirm their ethical alignment.</li>
            <li><strong>Training &amp; Awareness:</strong> Provide regular training on anti-corruption obligations and reporting procedures.</li>
            <li><strong>Whistleblowing &amp; Protection:</strong> Maintain confidential reporting channels and guarantee protection against retaliation.</li>
            <li><strong>Monitoring &amp; Auditing:</strong> Periodically review business practices to ensure compliance and identify areas for improvement.</li>
            <li><strong>Disciplinary Actions:</strong> Apply firm disciplinary or legal action in the event of breaches, including termination of contracts or referral to authorities.</li>
        </ul>

        <p>
            WWTT’s senior leadership ensures that adequate resources, oversight, and measurable objectives support the
            enforcement of this Anti-Corruption Policy, and that it is communicated to employees, clients, and partners.
        </p>

        {{-- 2. Environmental Policy --}}
        <h2 class="h4 mt-5">2. Environmental Policy</h2>
        <p class="text-muted">In alignment with the United Nations Global Compact (UNGC) and ISO 14001 Standards</p>

        <p>
            This Environmental Policy defines WWTT’s commitment to environmental protection, sustainability, and
            responsible business practices across all operations, particularly in travel, event management, and logistics
            services delivered to UN agencies, NGOs, and corporate partners.
        </p>

        <h3 class="h5 mt-3">Key Commitments</h3>
        <ul>
            <li><strong>Climate Change &amp; Carbon Reduction:</strong> Reduce greenhouse gas emissions through route optimisation, hybrid/virtual events, and collaboration with low-carbon partners.</li>
            <li><strong>Resource Efficiency:</strong> Minimise energy, water, and material use in operations and events while prioritising renewable and efficient resources.</li>
            <li><strong>Waste Management:</strong> Prevent and reduce waste, maximise reuse and recycling, and adopt circular economic principles in event planning.</li>
            <li><strong>Sustainable Procurement:</strong> Source environmentally responsible goods and services and integrate sustainability criteria into tenders and contracts.</li>
            <li><strong>Pollution Prevention:</strong> Comply with or exceed all applicable environmental laws and prevent harmful emissions, discharges, or spills.</li>
            <li><strong>Climate Adaptation:</strong> Assess risks related to climate change (e.g. heatwaves, flooding, extreme weather) in operations and events.</li>
            <li><strong>Staff Engagement:</strong> Train employees on sustainability, encourage innovation, and promote eco-conscious behaviour.</li>
            <li><strong>Stakeholder Collaboration:</strong> Partner with clients and suppliers to promote shared sustainability goals.</li>
            <li><strong>Continuous Improvement:</strong> Monitor, audit, and report environmental performance, including through UNGC Communication on Progress (CoP).</li>
        </ul>

        {{-- 3. Equal Opportunities Policy --}}
        <h2 class="h4 mt-5">3. Equal Opportunities Policy</h2>
        <p class="text-muted">In alignment with the UNGC, ISO 26000 &amp; ILO Core Conventions</p>

        <p>
            WWTT is committed to fairness, inclusion, non-discrimination, and the protection of human rights across all
